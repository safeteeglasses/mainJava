package com.techelevator.model;

public class Survey {

	private String color;
	private String natureOfQuest;
	private String petPreference;
	private String emailAddress;
	private String language;
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getNatureOfQuest() {
		return natureOfQuest;
	}
	public void setNatureOfQuest(String natureOfQuest) {
		this.natureOfQuest = natureOfQuest;
	}
	public String getPetPreference() {
		return petPreference;
	}
	public void setPetPreference(String petPreference) {
		this.petPreference = petPreference;
	}
	public String getEmailAddress() {
		return emailAddress;
	}
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	
	
}
